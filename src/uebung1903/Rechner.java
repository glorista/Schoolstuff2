package uebung1903;

public class Rechner {
    public int addieren(int x, int y){
        return x+y;
    }
    public double addieren(double x, double y){
        return x+y;
    }

    public int addieren(int x, int y, int k){
        return x+y+k;
    }
    public double addieren(double x, double y, double k){
        return x+y+k;
    }
}
